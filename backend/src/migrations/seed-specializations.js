async function seed() {
  console.log(`Seeding ${allSpecialties.length} specializations...`);

  let created = 0;
  for (const name of allSpecialties) {
    const existing = await pool.query('SELECT id FROM specializations WHERE name = $1', [name]);
    if (existing.rows.length === 0) {
      await pool.query(
        'INSERT INTO specializations (name, is_active) VALUES ($1, true)',
        [name]
      );
      created++;
    }
  }

  console.log(`  ✅ ${created} nouvelles spécialités créées`);
  console.log(`  Total: ${allSpecialties.length} spécialités`);
  await pool.end();
}

seed().catch(e => { console.error(e.message); process.exit(1); });
